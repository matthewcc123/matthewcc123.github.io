//Content Viewer
var contentOverlay = document.querySelector(".content-overlay");
var contentWindow = document.querySelector(".content-window");
var contentCloseButton = document.querySelector(".content-close-btn");
var scrollContainer;

$(document).ready(function() {

    for (var i in gameList)
    {
        var row = `<div class="card" id="game-item">
                    <img src="Resources/Images/Games/${gameList[i].id}/thumb.png">
                    </div>`;
    
        
        document.querySelector("#game-list").innerHTML += row;

    }

    for (var i in projectList)
    {
        var row = `<div class="card" id="project-item">
        <img src="Resources/Images/Projects/${projectList[i].id}/thumb.png">
                    </div>`;
    
        
        document.querySelector("#project-list").innerHTML += row;

    }

    for (var i in artList)
    {
        var row = `<a class="card" href="Resources/Images/Arts/${artList[i].id}/hd.png" data-lightbox="${artList[i].id}">
        <img src="Resources/Images/Arts/${artList[i].id}/thumb.png">
        </a>`;
    
        document.querySelector("#art-list").innerHTML += row;

    }

    for (var i in ytList)
    {
        var row = `<a class="card" href="${ytList[i].link}" target="_blank">
        <img src="Resources/Images/Youtube/${ytList[i].id}/thumb.png">
        </a>`;
    
        document.querySelector("#yt-list").innerHTML += row;

    }

    //Update Content
    function UpdateContent(id, list)
    {
        var title = document.querySelector(".content-title");
        var sub = document.querySelector(".content-sub");
        var content = document.querySelector(".content-content");

        title.innerHTML = list[id].name;
        sub.innerHTML = list[id].sub;
        content.innerHTML = list[id].content;
        scrollContainer = document.querySelector(".slider-wrapper");

        if (scrollContainer != null)
        {
            scrollContainer.addEventListener("wheel", (evt) => {
                evt.preventDefault();
                scrollContainer.scrollLeft += evt.deltaY;
                console.log(evt.deltaY);
            });
        }

    }

    function CloseWindow()
    {
        contentOverlay.classList.remove("active");
        contentWindow.classList.remove("active");
    }

    //Close Button
    contentCloseButton.addEventListener("click", ()=> {
        CloseWindow();
    });

    /*
    window.onclick = function (event) {
        if (event.target.contains(contentWindow) && event.target !== contentWindow) {
            CloseWindow();
        }
    }*/

    var gameItems = document.querySelectorAll("#game-item");

    gameItems.forEach((item, index) => {

        item.addEventListener('click', () => {
            contentOverlay.classList.add("active");
            contentWindow.classList.add("active");
            UpdateContent(index, gameList);
        });

    });

    var projectItems = document.querySelectorAll("#project-item");

    projectItems.forEach((item, index) => {

        item.addEventListener('click', () => {
            contentOverlay.classList.add("active");
            contentWindow.classList.add("active");
            UpdateContent(index, projectList);
        });

    });

});
